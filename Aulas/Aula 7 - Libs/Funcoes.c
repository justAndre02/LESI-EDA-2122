 /**
  * @brief Calcula a soma de dos valores inteiros.
  *
  * @param x
  * @param y
  * @return
  */
int Soma(int x, int y) {
	return (x + y);
}

/**
 * @brief .
 *
 * @param x
 * @param y
 * @return
 */
int Sub(int x, int y) {
	return (x - y);
}
